local streetName, crossing, currentZone

Citizen.CreateThread(function()
	while true do
		Citizen.Wait(500)

		local pos = GetEntityCoords(PlayerPedId())
		streetName, crossing = GetStreetNameAtCoord(pos.x, pos.y, pos.z)
		streetName, crossing = GetStreetNameFromHashKey(streetName), GetStreetNameFromHashKey(crossing)

		currentZone = zones[GetNameOfZone(pos.x, pos.y, pos.z)]
	end
end)

Citizen.CreateThread(function()
	while true do
		Citizen.Wait(0)

		if streetName and currentZone then
			drawTxt(0.3, 1.4, 0.45, '~s~ID:~y~ ' .. GetPlayerServerId(PlayerId()), 185, 185, 185, 255)

			if crossing == "" then
				drawTxt(0.734, 1.45, 0.45, currentZone, 185, 185, 185, 255)
			else
				drawTxt(0.734, 1.45, 0.45, crossing .. ', ' .. currentZone, 185, 185, 185, 255)
			end
			
			drawTxt(0.734, 1.42, 0.55, streetName, 240, 200, 80, 255)
		else
			Citizen.Wait(500)
		end
	end

end)

function drawTxt(x, y, scale, text, red, green, blue, alpha)
	SetTextFont(4)
	SetTextProportional(1)
	SetTextScale(scale, scale)
	SetTextColour(red, green, blue, alpha)
	SetTextDropShadow(0, 0, 0, 0, 255)
	SetTextEdge(1, 0, 0, 0, 255)
	SetTextDropShadow()
	SetTextOutline()

	BeginTextCommandDisplayText("STRING")
	AddTextComponentSubstringPlayerName(text)
	EndTextCommandDisplayText(0.175, y - 1 / 2 - 0.065)
end